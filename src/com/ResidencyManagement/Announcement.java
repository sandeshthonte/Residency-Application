    private void btnAnnouncementSubmit2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAnnouncementSubmit2ActionPerformed
        // TODO add your handling code here:
        String Wing = txtWing.getText();
        String Topic = txtTopic.getText();
        String Announcement = jTextArea1.getText();
        try{
            MongoClient mongo = new MongoClient( "localhost" , 27017 ); 
             MongoDatabase db = mongo.getDatabase("RESIDEM");
             //db.createCollection("AnnouncementsDB");
             MongoCollection<Document> AnnouncementsDB = db.getCollection("AnnouncementsDB");
             DateFormat df = new SimpleDateFormat("dd/MM/yy HH:mm:ss");
            Date dateobj = new Date();
//            System.out.println(df.format(dateobj));
            String postDate = df.format(dateobj);
            int generatedAnnID;
            while(true){
                    Random rand = new Random();
                    generatedAnnID = rand.nextInt(100000); 
                    FindIterable<Document> iterable =  AnnouncementsDB.find ( Filters.eq("annid", generatedAnnID) );
                    MongoCursor<Document> cursor = iterable.iterator();
                    if(!(cursor.hasNext())){
                        break;
                    }
           }
            
            AnnouncementsDB.insertOne(new Document("annid" , generatedAnnID).append("date", postDate).append("topic", Topic).append("wing", Wing).append("announcement", Announcement));
            //txtCreateIssuesFirstName.setText("");
            //jTextArea2.setText("");
            this.dispose();
            new Announcement(username).setVisible(true);
             infoMessage("Announcement #" + generatedAnnID   + " Added!", "Announcement Posted Successfully!");
                  
        }
        catch(Exception e)
        {
            System.out.println(e);
        }
    }//GEN-LAST:event_btnAnnouncementSubmit2ActionPerformed

