    private void btnDeleteAnnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteAnnActionPerformed
        // TODO add your handling code here:
        String s=jTextField1.getText();
        int id=Integer.parseInt(s);
        try{
            MongoClient mongo = new MongoClient( "localhost" , 27017 ); 
             MongoDatabase db = mongo.getDatabase("RESIDEM");
             //db.createCollection("IssuesDB");
             MongoCollection<Document> AnnouncementsDB = db.getCollection("AnnouncementsDB");
             
                    FindIterable<Document> iterable =  AnnouncementsDB.find (Filters.eq("annid",id));
                    MongoCursor<Document> cursor = iterable.iterator();
                    if(cursor.hasNext()){
                        AnnouncementsDB.deleteOne(Filters.eq("annid",id));
                        infoMessage("Announcement #"+id+" deleted!","Success");
                        this.dispose();
                        new Announcement(username).setVisible(true);
                    }
                    else
                    {
                        infoMessage("Announcement #"+id+" Not Found!","Failure");
                    }
                   
                  //mongo.close();
                   
        }
        catch(Exception e){
            System.out.print(e);
        }
    }//GEN-LAST:event_btnDeleteAnnActionPerformed

